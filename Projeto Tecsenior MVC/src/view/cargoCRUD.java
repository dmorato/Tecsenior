    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed

        Cargo car = new Cargo();
        CargoDAO dao = new CargoDAO();

        car.setNome(txtnome.getText());
        car.setSalario(Double.parseDouble(txtsalario.getText()));
        car.setCarga_horaria(Integer.parseInt(txtcarga_horaria.getText()));
        car.setDescricao(txadescricao.getText());

        dao.create(car);

        txtnome.setText("");
        txtsalario.setText("");
        txtcarga_horaria.setText("");
        txadescricao.setText("");

        readJTable();
    }//GEN-LAST:event_btncadastrarActionPerformed

    private void btnatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnatualizarActionPerformed
        if (jtcargo.getSelectedRow() != -1) {

            Cargo car = new Cargo();
            CargoDAO dao = new CargoDAO();

            car.setNome(txtnome.getText());
            car.setSalario(Double.parseDouble(txtsalario.getText()));
            car.setCarga_horaria(Integer.parseInt(txtcarga_horaria.getText()));
            car.setDescricao(txadescricao.getText());
            car.setId((int) jtcargo.getValueAt(jtcargo.getSelectedRow(), 0));
            dao.update(car);

            txtnome.setText("");
            txtsalario.setText("");
            txtcarga_horaria.setText("");
            txadescricao.setText("");

            readJTable();
        }
    }//GEN-LAST:event_btnatualizarActionPerformed

    private void btndeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeletarActionPerformed
        if (jtcargo.getSelectedRow() != -1) {
            Cargo car = new Cargo();
            CargoDAO dao = new CargoDAO();

            car.setId((int) jtcargo.getValueAt(jtcargo.getSelectedRow(), 0));
            dao.delete(car);

            txtnome.setText("");
            txtsalario.setText("");
            txtcarga_horaria.setText("");
            txadescricao.setText("");

            readJTable();
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_btndeletarActionPerformed

