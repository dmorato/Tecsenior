    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtcargo.getModel();
        modelo.setNumRows(0);
        CargoDAO cardao = new CargoDAO();

        for (Cargo car : cardao.read()) {
            modelo.addRow(new Object[]{
                car.getId(),
                car.getNome(),
                car.getSalario(),
                car.getCarga_horaria(),
                car.getDescricao()
            });
        }
    }

